
class Animal :
    def __init__(self,name,age,health_level,happiess_level,gender):
        self.name = name
        self.age = age
        self.gender = gender
        self.health_level = health_level
        self.happiess_level = happiess_level
    def display_info(self):
        print(f"Name: {self.name} - Age: {self.age} - Gender: {self.gender} - Health Level: {self.health_level} - Happinss Level: {self.happiess_level}")










